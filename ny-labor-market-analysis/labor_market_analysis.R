# SERVER
# ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
server <- function(input, output, session) {
  
  # Reactive filter
  filtered <- reactive({
    df %>%
      filter(Year >= input$sel_years[1],
             Year <= input$sel_years[2],
             Era  %in% input$sel_era)
  })
  
  # ?????? VALUE BOXES ??????????????????????????????????????????????????????????????????????????????????????????
  output$vb_avg_unemp <- renderValueBox({
    avg <- round(mean(filtered()$`Unemployment Rate`), 2)
    valueBox(paste0(avg, "%"), "Avg Unemployment Rate", icon=icon("percent"), color="blue")
  })
  output$vb_peak_unemp <- renderValueBox({
    peak     <- filtered() %>% slice_max(`Unemployment Rate`, n=1) %>% slice(1)
    peak_val <- as.character(peak$`Unemployment Rate`[1])
    peak_lbl <- paste0("Peak Rate (", format(as.Date(peak$Date[1]), "%b %Y"), ")")
    valueBox(paste0(peak_val, "%"), peak_lbl, icon=icon("arrow-up"), color="red")
  })
  output$vb_low_unemp <- renderValueBox({
    low     <- filtered() %>% slice_min(`Unemployment Rate`, n=1) %>% slice(1)
    low_val <- as.character(low$`Unemployment Rate`[1])
    low_lbl <- paste0("Lowest Rate (", format(as.Date(low$Date[1]), "%b %Y"), ")")
    valueBox(paste0(low_val, "%"), low_lbl, icon=icon("arrow-down"), color="green")
  })
  output$vb_recovery <- renderValueBox({
    n_months <- nrow(filtered())
    valueBox(n_months, "Months of Data", icon=icon("calendar"), color="purple")
  })
  
  # ?????? OVERVIEW: Timeline ?????????????????????????????????????????????????????????????????????
  output$plot_timeline <- renderPlotly({
    d <- filtered()
    p <- ggplot(d, aes(x=Date, y=`Unemployment Rate`,
                       color=Era,
                       text=paste0(format(Date,"%b %Y"),
                                   "\nRate: ", `Unemployment Rate`, "%",
                                   "\nEra: ", Era))) +
      geom_line(aes(group=1), color="#c0392b", linewidth=1) +
      geom_point(aes(color=Era), size=2) +
      scale_color_manual(values=ERA_COLORS) +
      scale_x_date(date_breaks="1 year", date_labels="%Y") +
      scale_y_continuous(labels=function(x) paste0(x,"%")) +
      labs(x=NULL, y="Unemployment Rate (%)", color="Era") +
      theme_minimal(base_size=12) +
      theme(panel.grid.minor=element_blank(), legend.position="bottom")
    ggplotly(p, tooltip="text") %>%
      layout(legend=list(orientation="h", y=-0.2))
  })
  
  # ?????? OVERVIEW: Era bar ????????????????????????????????????????????????????????????????????????
  output$plot_era_bar <- renderPlotly({
    d <- filtered() %>%
      group_by(Era) %>%
      summarise(Avg=round(mean(`Unemployment Rate`),2), .groups="drop") %>%
      arrange(Era)
    
    plot_ly(d,
            x = ~Avg,
            y = ~Era,
            type = "bar",
            orientation = "h",
            marker = list(color = ERA_COLORS[as.character(d$Era)]),
            text  = ~paste0(Avg, "%"),
            textposition = "outside",
            hovertemplate = "%{y}: %{x}%<extra></extra>") %>%
      layout(
        xaxis = list(title="Avg Unemployment Rate (%)",
                     ticksuffix="%",
                     range=c(0, max(d$Avg, na.rm=TRUE) * 1.3)),
        yaxis = list(title=NULL, autorange="reversed"),
        showlegend = FALSE,
        margin = list(l=120, r=60, t=20, b=50)
      )
  })
  
  # ?????? TRENDS: Metric line ??????????????????????????????????????????????????????????????????
  output$plot_metric_line <- renderPlotly({
    metric <- input$sel_metric
    d <- filtered()
    p <- ggplot(d, aes(x=Date, y=.data[[metric]],
                       text=paste0(format(Date,"%b %Y"),"\n",metric,": ",.data[[metric]],"%"))) +
      geom_line(color="#2980b9", linewidth=1.1) +
      geom_area(fill="#2980b9", alpha=0.12) +
      scale_x_date(date_breaks="1 year", date_labels="%Y") +
      scale_y_continuous(labels=function(x) paste0(x,"%")) +
      labs(x=NULL, y=paste0(metric," (%)")) +
      theme_minimal(base_size=12) +
      theme(panel.grid.minor=element_blank())
    ggplotly(p, tooltip="text")
  })
  
  # ?????? TRENDS: Yearly avg bar ?????????????????????????????????????????????????????????
  output$plot_yearly_avg <- renderPlotly({
    metric <- input$sel_metric
    d <- filtered() %>%
      group_by(Year) %>%
      summarise(Avg=round(mean(.data[[metric]]),2), .groups="drop")
    p <- ggplot(d, aes(x=factor(Year), y=Avg, fill=Avg,
                       text=paste("Year:",Year,"\nAvg:",Avg,"%"))) +
      geom_col(width=0.75, show.legend=FALSE) +
      scale_fill_gradient2(low="#27ae60", mid="#f39c12", high="#e74c3c", midpoint=5.5) +
      scale_y_continuous(expand=expansion(mult=c(0,0.12)),
                         labels=function(x) paste0(x,"%")) +
      labs(x=NULL, y=paste0("Avg ",metric," (%)")) +
      theme_minimal(base_size=11) +
      theme(panel.grid.major.x=element_blank(),
            axis.text.x=element_text(angle=45,hjust=1))
    ggplotly(p, tooltip="text")
  })
  
  # ?????? TRENDS: Employed area ????????????????????????????????????????????????????????????
  output$plot_employed_area <- renderPlotly({
    d <- filtered() %>%
      select(Date, Employed, Unemployed) %>%
      pivot_longer(-Date, names_to="Status", values_to="Count")
    p <- ggplot(d, aes(x=Date, y=Count, fill=Status,
                       text=paste0(format(Date,"%b %Y"),"\n",Status,": ",
                                   format(Count,big.mark=",")))) +
      geom_area(alpha=0.75, position="identity") +
      scale_fill_manual(values=c("Employed"="#2980b9","Unemployed"="#e74c3c"), name=NULL) +
      scale_x_date(date_breaks="1 year", date_labels="%Y") +
      scale_y_continuous(labels=comma) +
      labs(x=NULL, y="Number of Persons") +
      theme_minimal(base_size=11) +
      theme(panel.grid.minor=element_blank(), legend.position="bottom")
    ggplotly(p, tooltip="text") %>%
      layout(legend=list(orientation="h", y=-0.2))
  })
  
  # ?????? TRENDS: LFPR line ????????????????????????????????????????????????????????????????????????
  output$plot_lfp <- renderPlotly({
    d <- filtered()
    p <- ggplot(d, aes(x=Date, y=`Labor Force Participation Rate`,
                       text=paste0(format(Date,"%b %Y"),
                                   "\nLFPR: ",`Labor Force Participation Rate`,"%"))) +
      geom_line(color="#8e44ad", linewidth=1.1) +
      geom_area(fill="#8e44ad", alpha=0.1) +
      scale_x_date(date_breaks="1 year", date_labels="%Y") +
      scale_y_continuous(labels=function(x) paste0(x,"%"),
                         limits=c(65,70)) +
      labs(x=NULL, y="LFPR (%)") +
      theme_minimal(base_size=11) +
      theme(panel.grid.minor=element_blank())
    ggplotly(p, tooltip="text")
  })
  
  # ?????? SEASONAL: Era line ?????????????????????????????????????????????????????????????????????
  output$plot_seasonal_era <- renderPlotly({
    d <- filtered() %>%
      group_by(Month, Era) %>%
      summarise(Avg_Rate=round(mean(`Unemployment Rate`),2), .groups="drop") %>%
      mutate(MonthLabel=factor(month.abb[Month], levels=month.abb))
    p <- ggplot(d, aes(x=MonthLabel, y=Avg_Rate, group=Era, color=Era,
                       text=paste(Era,"\nMonth:",MonthLabel,"\nAvg:",Avg_Rate,"%"))) +
      geom_line(linewidth=1.1) +
      geom_point(size=3) +
      scale_color_manual(values=ERA_COLORS) +
      scale_y_continuous(labels=function(x) paste0(x,"%")) +
      labs(x=NULL, y="Avg Unemployment Rate (%)", color="Era") +
      theme_minimal(base_size=12) +
      theme(panel.grid.minor=element_blank(), legend.position="bottom")
    ggplotly(p, tooltip="text") %>%
      layout(legend=list(orientation="h", y=-0.2))
  })
  
  # ?????? SEASONAL: Heatmap ????????????????????????????????????????????????????????????????????????
  output$plot_heatmap <- renderPlotly({
    d <- filtered() %>%
      mutate(MonthLabel=factor(month.abb[Month], levels=month.abb))
    plot_ly(d, x=~MonthLabel, y=~factor(Year),
            z=~`Unemployment Rate`,
            type="heatmap",
            colorscale=list(c(0,"#d5e8d4"), c(0.5,"#f39c12"), c(1,"#c0392b")),
            colorbar=list(title="Rate (%)"),
            hovertemplate="%{y} %{x}<br>Rate: %{z}%<extra></extra>") %>%
      layout(xaxis=list(title=NULL),
             yaxis=list(title=NULL),
             margin=list(l=50,b=40))
  })
  
  # ?????? FORECAST: Plot ?????????????????????????????????????????????????????????????????????????????????
  output$plot_forecast <- renderPlotly({
    hist <- df %>% select(Date, Rate=`Unemployment Rate`) %>%
      mutate(Type="Historical", Lo80=NA, Hi80=NA, Lo95=NA, Hi95=NA)
    fore <- fc_df %>% rename(Rate=Forecast) %>% mutate(Type="Forecast")
    combined <- bind_rows(hist, fore)
    
    plot_ly() %>%
      add_ribbons(data=fore, x=~Date, ymin=~Lo95, ymax=~Hi95,
                  fillcolor="rgba(41,128,185,0.15)", line=list(color="transparent"),
                  name="95% CI") %>%
      add_ribbons(data=fore, x=~Date, ymin=~Lo80, ymax=~Hi80,
                  fillcolor="rgba(41,128,185,0.30)", line=list(color="transparent"),
                  name="80% CI") %>%
      add_lines(data=hist, x=~Date, y=~Rate,
                line=list(color="#c0392b", width=2), name="Historical") %>%
      add_lines(data=fore, x=~Date, y=~Rate,
                line=list(color="#2980b9", width=2, dash="dash"), name="Forecast") %>%
      layout(
        xaxis=list(title=NULL, tickformat="%Y"),
        yaxis=list(title="Unemployment Rate (%)",
                   ticksuffix="%"),
        legend=list(orientation="h", y=-0.15),
        hovermode="x unified"
      )
  })
  
  # ?????? FORECAST: Table ??????????????????????????????????????????????????????????????????????????????
  output$table_forecast <- renderDT({
    fc_df %>%
      mutate(
        Date     = format(Date, "%B %Y"),
        Forecast = paste0(round(Forecast,2), "%"),
        Lo80     = paste0(round(Lo80,2), "%"),
        Hi80     = paste0(round(Hi80,2), "%")
      ) %>%
      select(Month=Date, `Point Forecast`=Forecast,
             `Lower 80% CI`=Lo80, `Upper 80% CI`=Hi80) %>%
      datatable(rownames=FALSE, options=list(pageLength=12, dom="tip"),
                class="compact stripe")
  })
  
  # ?????? DATA EXPLORER ????????????????????????????????????????????????????????????????????????????????????
  output$table_main <- renderDT({
    filtered() %>%
      select(Date, Year, Month, `Unemployment Rate`, `Employment Rate`,
             `Labor Force Participation Rate`, Employed, Unemployed,
             `Civilian Labor Force`, Era) %>%
      arrange(desc(Date)) %>%
      datatable(rownames=FALSE, filter="top",
                options=list(pageLength=20, scrollX=TRUE, dom="lfrtip"),
                class="compact stripe")
  })
  
} # end server

shinyApp(ui=ui, server=server)