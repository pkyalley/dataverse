raw <- read_excel("data/Employment__Unemployment__and_Labor_Force_Data.xlsx")

df <- raw %>%
  mutate(
    Date  = as.Date(Date),
    Year  = as.integer(Year),
    Month = as.integer(Month),
    MonthLabel = factor(month.abb[Month], levels = month.abb),
    Era = case_when(
      Year <= 2008 ~ "Pre-Crisis",
      Year <= 2010 ~ "Crisis",
      Year <= 2013 ~ "Early Recovery",
      Year <= 2016 ~ "Late Recovery",
      TRUE         ~ "Expansion"
    ),
    Era = factor(Era, levels = c("Pre-Crisis","Crisis","Early Recovery","Late Recovery","Expansion"))
  )

# Pre-compute forecast (do once at startup)
unemp_ts <- ts(df$`Unemployment Rate`, start = c(2007,1), frequency = 12)
fit_arima <- auto.arima(unemp_ts, seasonal = TRUE, stepwise = TRUE, approximation = TRUE)
fc        <- forecast(fit_arima, h = 24, level = c(80, 95))

fc_df <- data.frame(
  Date     = seq(as.Date("2019-09-01"), by="month", length.out=24),
  Forecast = as.numeric(fc$mean),
  Lo80     = as.numeric(fc$lower[,1]),
  Hi80     = as.numeric(fc$upper[,1]),
  Lo95     = as.numeric(fc$lower[,2]),
  Hi95     = as.numeric(fc$upper[,2])
)

yearly <- df %>%
  group_by(Year) %>%
  summarise(
    Avg_Unemp  = round(mean(`Unemployment Rate`),2),
    Avg_LFP    = round(mean(`Labor Force Participation Rate`),2),
    Avg_Emp    = round(mean(Employed)),
    Avg_Unemployed = round(mean(Unemployed)),
    .groups="drop"
  )

ERA_COLORS <- c(
